export async function getProgramAction(programId: string) {
  const { userId } = await auth();
  if (!userId) return { success: false as const, error: "Unauthorized" };

  const dbUser = await prisma.user.findUnique({ where: { clerkId: userId } });
  if (!dbUser) return { success: false as const, error: "User not found" };

  try {
    const program = await programService.getProgramById(programId);
    if (!program)
      return { success: false as const, error: "Program not found" };

    if (program.trainerId !== dbUser.id && program.clientId !== dbUser.id) {
      return { success: false as const, error: "Forbidden" };
    }

    return { success: true as const, data: program };
  } catch (error) {
    console.error("Failed to fetch program:", error);
    return { success: false as const, error: "Failed to fetch program" };
  }
}


export async function generateProgramAction(
  params: {
    clientId?: string | null;
    startDate?: string | null;
    durationWeeks?: number;          // NEW
    weekPlan?: WeekPlan[];           // NEW
    [key: string]: unknown;
  }
) {
  const user = await getTrainerUser();
  if (!user) return { success: false as const, error: "Unauthorized" };

  try {
    const aiPlan = await generateProgram({
      ...params,
      weekPlan: params.weekPlan,
      durationWeeks: params.durationWeeks,
    } as Parameters<typeof generateProgram>[0]);
    const program = await createProgramFromGeneratedPlan({
      aiPlan,
      trainerId: user.id,
      isTemplate: false,
      aiGenerationParams: params,
      clientId: params.clientId || null,
      startDate: params.clientId
        ? (params.startDate ?? new Date().toISOString().split("T")[0])
        : null,
    });

    revalidatePath("/programs");
    if (params.clientId) revalidatePath(`/clients/${params.clientId}`);
    return { success: true as const, data: program.id };
  } catch (error) {
    console.error("Failed to generate program:", error);
    return { success: false as const, error: "Failed to generate program" };
  }
}

export async function generateProgramPreviewFromBriefAction(input: {
  fileUrl: string;
  fileName: string;
}) {
  const user = await getTrainerUser();
  if (!user) return { success: false as const, error: "Unauthorized" };

  try {
    const rawText = await extractProgramBriefText(input.fileUrl, input.fileName);
    const parsed = await parseProgramBriefFlexible(rawText);
    if (!parsed.ok) {
      return { success: false as const, error: parsed.errors.join("\n") };
    }

    const brief = parsed.data;

    const params = {
      programTitle: brief.programTitle,
      focusAreas: brief.focusAreas,
      durationMinutes: brief.durationMinutes,
      daysPerWeek: brief.daysPerWeek,
      circuits: brief.circuits.map((c) => ({
        name: c.name,
        focusType: c.focusType,
        exerciseCount: c.exerciseCount,
        rounds: c.rounds,
      })),
      difficultyLevel: brief.difficultyLevel,
      preferredWeekdays: brief.preferredWeekdays,
      additionalNotes: brief.additionalNotes,
      subjective: brief.subjective,
      trainerPrompt: brief.trainerPrompt,
      preferredExerciseNames: brief.preferredExerciseNames,
      sessionBlueprint: brief.sessionBlueprint,
    };

    const aiPlan = await generateProgram(params);

    return {
      success: true as const,
      data: {
        aiPlan,
        params,
        parsed: brief,
      },
    };
  } catch (error) {
    console.error("Failed to generate program from brief:", error);
    return {
      success: false as const,
      error: "Failed to generate program from brief",
    };
  }
}

