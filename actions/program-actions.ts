async function getTrainerUser() {
  const { userId } = await auth();
  if (!userId) return null;
  const dbUser = await prisma.user.findUnique({ where: { clerkId: userId } });
  if (!dbUser || dbUser.role !== "TRAINER") return null;
  return dbUser;
}

async function createProgramFromGeneratedPlan(params: {
  aiPlan: GeneratedProgram;
  trainerId: string | null;
  isGlobal?: boolean;
  isTemplate: boolean;
  aiGenerationParams: Record<string, unknown>;
  clientId?: string | null;
  startDate?: string | null;
}) {
  const { aiPlan, trainerId, isTemplate, aiGenerationParams, clientId, startDate } = params;

  const sDate = startDate
    ? (() => { const [y, m, d] = startDate.split("-").map(Number); return new Date(y, m - 1, d); })()
    : null;

  // Round 1: program shell only — no nested creates
  const program = await prisma.program.create({
    data: {
      name: aiPlan.name,
      description: aiPlan.description || "Generated by AI",
      isTemplate: !clientId && isTemplate,
      isGlobal: params.isGlobal ?? false,
      trainerId,
      clientId: clientId ?? null,
      status: clientId ? "ACTIVE" : "DRAFT",
      startDate: sDate ?? undefined,
      aiGenerationParams: aiGenerationParams as import("@prisma/client").Prisma.InputJsonValue,
    },
    select: { id: true },
  });

  // Round 2: all workouts in parallel
  const createdWorkouts = await Promise.all(
    aiPlan.workouts.map((w) =>
      prisma.workout.create({
        data: {
          programId: program.id,
          name: w.name,
          dayIndex: w.dayIndex,
          weekIndex: w.weekIndex ?? 0,
          orderIndex: (w.weekIndex ?? 0) * 7 + w.dayIndex,
        },
        select: { id: true, dayIndex: true },
      })
    )
  );

  // Round 3: all blocks in parallel — flat list keyed by workout index
  const blockInputs = aiPlan.workouts.flatMap((w, wi) =>
    w.blocks.map((b) => ({ workoutId: createdWorkouts[wi].id, block: b }))
  );
  const createdBlocks = await Promise.all(
    blockInputs.map(({ workoutId, block: b }) =>
      prisma.workoutBlockV2.create({
        data: {
          workoutId,
          type: b.type || "NORMAL",
          name: b.name || b.type || "NORMAL",
          orderIndex: b.orderIndex,
          rounds: b.rounds ?? 1,
          restBetweenRounds: b.restBetweenRounds ?? null,
        },
        select: { id: true },
      })
    )
  );

  // Round 4: all exercises in parallel — flat list keyed by block index
  const exerciseInputs = blockInputs.flatMap(({ block: b }, bi) =>
    b.exercises.map((e) => ({ blockId: createdBlocks[bi].id, exercise: e }))
  );
  const createdExercises = await Promise.all(
    exerciseInputs.map(({ blockId, exercise: e }) =>
      prisma.blockExerciseV2.create({
        data: {
          blockId,
          exerciseId: e.exerciseId,
          orderIndex: e.orderIndex,
          notes: e.notes ?? null,
          restSeconds: e.restSeconds ?? null,
        },
        select: { id: true },
      })
    )
  );

  // Round 5: all sets in ONE createMany — fastest possible for the bulk records
  const allSets = exerciseInputs.flatMap(({ exercise: e }, ei) => {
    const repStr = e.reps?.toString() ?? "10";
    const isDuration = repStr.endsWith("s");
    const targetReps = isDuration ? null : parseInt(repStr, 10) || null;
    const targetDuration = isDuration ? parseInt(repStr.replace("s", ""), 10) || null : null;
    return Array.from({ length: Math.max(1, e.sets || 1) }).map((_, i) => ({
      blockExerciseId: createdExercises[ei].id,
      orderIndex: i,
      setType: "NORMAL" as const,
      ...(targetReps !== null ? { targetReps } : {}),
      ...(targetDuration !== null ? { targetDuration } : {}),
    }));
  });
  if (allSets.length > 0) {
    await prisma.exerciseSet.createMany({ data: allSets });
  }

  // Round 6: sessions if assigning — single createMany
  if (clientId && sDate) {
    // Build a map from createdWorkout id → aiPlan workout (to get weekIndex)
    const workoutWeekMap = new Map<string, number>(
      aiPlan.workouts.map((w, i) => [createdWorkouts[i].id, w.weekIndex ?? 0])
    );
    await prisma.workoutSessionV2.createMany({
      data: createdWorkouts.map((w) => {
        const weekIdx = workoutWeekMap.get(w.id) ?? 0;
        const d = new Date(sDate);
        d.setDate(d.getDate() + weekIdx * 7 + w.dayIndex);
        return { workoutId: w.id, clientId, scheduledDate: d, status: "SCHEDULED" as const };
      }),
    });
  }

  return program;
}

export async function createProgramAction(input: CreateProgramInput) {
  const user = await getTrainerUser();
  if (!user) return { success: false as const, error: "Unauthorized" };

  const parsed = createProgramSchema.safeParse(input);
  if (!parsed.success) {
    return { success: false as const, error: parsed.error.issues[0].message };
  }

  try {
    const program = await programService.createProgram(user.id, parsed.data);
    revalidatePath("/programs");
    return { success: true as const, data: program };
  } catch (error) {
    console.error("Failed to create program:", error);
    return { success: false as const, error: "Failed to create program" };
  }
}

export async function updateProgramAction(
  programId: string,
  input: UpdateProgramInput
) {
  const user = await getTrainerUser();
  if (!user) return { success: false as const, error: "Unauthorized" };

  const program = await prisma.program.findUnique({
    where: { id: programId },
    select: { trainerId: true },
  });
  if (!program || program.trainerId !== user.id) {
    return { success: false as const, error: "Forbidden" };
  }

  const parsed = updateProgramSchema.safeParse(input);
  if (!parsed.success) {
    return { success: false as const, error: parsed.error.issues[0].message };
  }

  try {
    const updated = await programService.updateProgram(programId, parsed.data);
    revalidatePath("/programs");
    revalidatePath(`/programs/${programId}`);
    return { success: true as const, data: updated };
  } catch (error) {
    console.error("Failed to update program:", error);
    return { success: false as const, error: "Failed to update program" };
  }
}

